#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	len;

	if (dest == NULL || src == NULL)
		return (0);
	len = ft_strlen(src);
	if (size != 0)
	{
		i = 0;
		while ((i < len) && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (len);
}
